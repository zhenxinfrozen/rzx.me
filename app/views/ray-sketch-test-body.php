<script>
(function($){
  $(function(){
    var $gallery = $('#pp_gallery');
    var $albums  = $('#pp_thumbContainer .album');
    var $loader  = $('#pp_loading');
    var $nextBtn = $('#pp_next');
    var $prevBtn = $('#pp_prev');
    var $backBtn = $('#pp_back');

    var enableshow = true;
    var current = -1; // 当前图片 index（content 在相册中的 index）
    var album   = -1; // 当前相册 index

    // 预加载缩略图后进行布局
    var thumbs = $('#pp_thumbContainer .content img');
    var totalThumbs = thumbs.length, loaded = 0;
    thumbs.each(function(){
      var img = new Image();
      img.onload = function(){ if(++loaded === totalThumbs) layoutAlbums(); };
      img.src = this.src;
    });

        function layoutAlbums(){
            var w = $(window).width();
            var spaces = w/($albums.length+1);
            var cnt = 0;
            $albums.each(function(){
                var $a = $(this);
                cnt++;
                var left = spaces*cnt - ($a.width()/2);
                $a.css({ left:left, bottom:0 }).on('click', spreadPictures);
            });
      thumbs.each(function(){
        var r = Math.floor(Math.random()*41)-20;
        $(this).css({ rotate: r+'deg' }); // 由 jquery.transform 处理
      });
    }

        function spreadPictures(){
      var $album = $(this);
      album = $albums.index($album);
      $albums.not($album).stop(true).animate({ bottom:-150 }, 500);
      $album.off('click', spreadPictures).data('left', $album.css('left')).animate({ left:0 }, 400);
      $album.find('.descr').stop(true).animate({ bottom:-30 }, 400);

                    // 保持缩略图在当前相册内，通过窗口宽度均分定位（与旧版一致）
                    var $contents = $album.find('.content');
                    var total = $contents.length;
                    var w = $(window).width();
                    var spaces = w/(total+1);
                    var cnt = 0;
                    $contents.each(function(){
                        cnt++;
                        var left = (spaces*cnt) - (140/2);
                        var r = Math.floor(Math.random()*41)-20;
                        $(this).stop(true).animate({ left:left }, 500, function(){
                            $(this).off('click').on('click', showImage)
                                         .off('mouseenter').on('mouseenter', upImage)
                                         .off('mouseleave').on('mouseleave', downImage);
                        }).find('img').stop().css({ rotate: r+'deg' });
                    });
      $backBtn.stop(true).animate({ left:0 }, 400);
    }

        $backBtn.on('click', function(){
      $backBtn.stop(true).animate({ left:-100 }, 300);
      hideNavigation();
      var $currentAlbum = $albums.eq(album);
      if($currentAlbum.length){
        $currentAlbum.animate({ left: $currentAlbum.data('left')||0 }, 400)
                     .find('.descr').animate({ bottom:0 }, 400);
        $currentAlbum.on('click', spreadPictures);
                        // 重置当前相册内缩略图位置与事件（无需移动 DOM）
                        $currentAlbum.find('.content').each(function(){
                            $(this).stop(true)
                                         .off('click', showImage)
                                         .off('mouseenter mouseleave')
                                         .css({ left:0, marginTop:0 });
                        });
      }
      $albums.not($currentAlbum).stop(true).animate({ bottom:0 }, 400);
    });

    function showImage(nav){
      if(!enableshow) return; enableshow=false;
      var $content;
      if(nav===1){
        if(current===0){ enableshow=true; return; }
        var $album = $albums.eq(album);
        var $list = $album.find('.content');
        $content = $($list.get(current-1));
        if(!$content.length){ enableshow=true; current-=2; return; }
      } else {
        $content = $(this);
      }

      $loader.show();
      current = $content.index();
      var src = $content.find('img').attr('alt');
      var caption = $content.find('span').text();
      var $large = $('<img/>', { src: src });

      $large.on('load', function(){
        resizeNative(this);
        function doCreate(){
          var $exist = $gallery.find('.pp_preview');
          if($exist.length) $exist.remove();
          var $preview = $('<div/>', { id:'pp_preview', 'class':'pp_preview' });
          var $descr = $('<div/>', { 'class':'pp_descr' }).html('<span>'+caption+'</span>');
          $preview.append($large).append($descr);
          $gallery.prepend($preview);
          var largeW = $large[0].width + 20;
          var largeH = $large[0].height + 10 + 45;
          $preview.css({ width:largeW, height:largeH, visibility:'visible' });
          if(window.Cufon){ try{ Cufon.replace('.pp_descr'); }catch(e){} }
          showNavigation(); $loader.hide();
          var r = Math.floor(Math.random()*41)-20;
          $preview.css('--pp-rot', r+'deg');
          // 强制 reflow 后添加 enter 以触发 CSS 过渡
          $preview[0].offsetWidth; $preview.addClass('enter');
          enableshow=true;
        }
        if($('#pp_preview').length) hideCurrentPicture(doCreate); else doCreate();
      });
    }

    $nextBtn.on('click', function(){ current+=2; showImage(1); });
    $prevBtn.on('click', function(){ showImage(1); });

    function hideCurrentPicture(done){
      current = -1;
      var $pp = $('#pp_preview');
      if(!$pp.length){ if($.isFunction(done)) done(); return; }
      $pp.removeClass('enter'); $pp[0].offsetWidth; $pp.addClass('exit');
      var called=false;
      $pp.on('transitionend', function(){ if(called) return; called=true; $pp.off('transitionend'); $pp.remove(); if($.isFunction(done)) done(); });
      setTimeout(function(){ if(!called){ called=true; $pp.off('transitionend'); $pp.remove(); if($.isFunction(done)) done(); } }, 700);
    }

    function showNavigation(){ $nextBtn.stop(true).animate({ right:0 }, 300); $prevBtn.stop(true).animate({ left:0 }, 300); }
    function hideNavigation(){ $nextBtn.stop(true).animate({ right:-40 }, 300); $prevBtn.stop(true).animate({ left:-40 }, 300); }
    function upImage(){ $(this).stop(true).animate({ marginTop:-70 }, 200).find('img').css({ rotate:'0deg' }); }
    function downImage(){ var r=Math.floor(Math.random()*41)-20; $(this).stop(true).animate({ marginTop:0 }, 200).find('img').css({ rotate: r+'deg' }); }

    function resizeNative(imageEl){
      var widthMargin=50, heightMargin=200;
      var windowH=$(window).height()-heightMargin;
      var windowW=$(window).width()-widthMargin;
      var theImage=new Image(); theImage.src=imageEl.src;
      var imgwidth=theImage.width, imgheight=theImage.height;
      if((imgwidth>windowW)||(imgheight>windowH)){
        if(imgwidth>imgheight){
          var newwidth=windowW; var ratio=imgwidth/windowW; var newheight=imgheight/ratio; theImage.height=newheight; theImage.width=newwidth;
          if(newheight>windowH){ var newnewheight=windowH; var newratio=newheight/windowH; var newnewwidth=newwidth/newratio; theImage.width=newnewwidth; theImage.height=newnewheight; }
        } else {
          var newheight=windowH; var ratio2=imgheight/windowH; var newwidth2=imgwidth/ratio2; theImage.height=newheight; theImage.width=newwidth2;
          if(newwidth2>windowW){ var newnewwidth2=windowW; var newratio2=newwidth2/windowW; var newnewheight2=newheight/newratio2; theImage.height=newnewheight2; theImage.width=newnewwidth2; }
        }
      }
      $(imageEl).css({ width:theImage.width, height:theImage.height });
    }
  });
})(jQuery);
</script>
