        <script type="text/javascript">
				$(function() {
				// detect IE via documentMode (works for IE8+), fallback to false
				var ie = !!document.documentMode;
				//current album/image displayed 
				var enableshow  = true;
				var current		= -1;
				var album		= -1;
				//windows width
				var w_width 	= $(window).width();
				//caching
				var $albums 	= $('#pp_thumbContainer div.album');
				var $loader		= $('#pp_loading');
				var $next		= $('#pp_next');
				var $prev		= $('#pp_prev');
				var $images		= $('#pp_thumbContainer div.content img');
				var $back		= $('#pp_back');
				
				//we wnat to spread the albums through the page equally
				//number of spaces to divide with:number of albums plus 1
				var nmb_albums	= $albums.length;
				var spaces 		= w_width/(nmb_albums+1);
				// DEBUG: log layout info
				try{console.log('DEBUG: w_width=', w_width, 'nmb_albums=', nmb_albums);}catch(e){}
				var cnt			= 0;
				//preload all the images (thumbs)
				var nmb_images	= $images.length;
				var loaded  	= 0;
				$images.each(function(i){
					var $image = $(this);
					$('<img />').on('load', function(){
						++loaded;
						try{console.log('DEBUG: loaded=', loaded, 'nmb_images=', nmb_images);}catch(e){}
						if(loaded == nmb_images){
							//let's spread the albums equally on the bottom of the page
							$albums.each(function(){
								var $this 	= $(this);
								++cnt;
								var left	= spaces*cnt - $this.width()/2;
								try{console.log('DEBUG: album', cnt, 'left=', left, '$this.width=', $this.width());}catch(e){}
								$this.css('left',left+'px');
								$this.stop().animate({'bottom':'0px'},500);
								}).off('click').on('click',spreadPictures);
							//also rotate each picture of an album with a random number of degrees
							$images.each(function(){
								var $this 	= $(this);
								var r 		= Math.floor(Math.random()*41)-20;
								$this.transform({'rotate'	: r + 'deg'});
							});
						}
					}).attr('src', $image.attr('src'));
				});
				
				function spreadPictures(){
					var $album 	= $(this);
					//track which album is opened
					album		= $album.index();
					//hide all the other albums
					$albums.not($album).stop().animate({'bottom':'-150px'},300);
					$album.off('click');
					//now move the current album to the left 
					//and at the same time spread its images through 
					//the window, rotating them randomly. Also hide the description of the album
					
					//store the current left for the reverse operation
					$album.data('left',$album.css('left'))
						  .stop()
						  .animate({'left':'0px'},500).find('.descr').stop().animate({'bottom':'-30px'},200);
					var total_pic 	= $album.find('.content').length;
					var cnt			= 0;
					//each picture
					$album.find('.content')
						  .each(function(){
						var $content = $(this);
						++cnt;
						//window width
						var w_width 	= $(window).width();
						var spaces 		= w_width/(total_pic+1);
						var left		= (spaces*cnt) - (140/2);
						var r 			= Math.floor(Math.random()*41)-20;
						//var r = Math.floor(Math.random()*81)-40;
						$content.stop().animate({'left':left+'px'},500,function(){
					$(this).off('click')
						.on('click',showImage)
						.off('mouseenter')
						.on('mouseenter',upImage)
						.off('mouseleave')
						.on('mouseleave',downImage);
						}).find('img')
						  .stop()
						  .animate({'rotate': r+'deg'},300);
						$back.stop().animate({'left':'0px'},300);
					});
				}
				
				//back to albums
				//the current album gets its innitial left position
				//all the other albums slide up
				//the current image slides out
				$back.on('click',function(){
					$back.stop().animate({'left':'-100px'},300);
					hideNavigation();
					//there's a picture being displayed
					// (no action here; preview hide/wait handled when user clicks a thumb)
					
					var $current_album = $('#pp_thumbContainer div.album:nth-child('+parseInt(album+1)+')');
					$current_album.stop()
								  .animate({'left':$current_album.data('left')},500)
								  .find('.descr')
								  .stop()
								  .animate({'bottom':'0px'},500);
					
					$current_album.off('click')
								  .on('click',spreadPictures);
					
					$current_album.find('.content')
							  .each(function(){
								var $content = $(this);
								$content.off('mouseenter mouseleave click');
								$content.stop().animate({'left':'0px'},500);
								});
								
					$albums.not($current_album).stop().animate({'bottom':'0px'},500);
				});
				
				//displays an image (clicked thumb) in the center of the page
				//if nav is passed, then displays the next / previous one of the 
				//current album
				function showImage(nav){
					if(!enableshow) return;
					enableshow = false;
					if(nav == 1){
						//reached the first one
						if(current==0){
							enableshow = true;
							return;
						}
						var $content 			= $('#pp_thumbContainer div.album:nth-child('+parseInt(album+1)+')')
												  .find('.content:nth-child('+parseInt(current)+')');
						//reached the last one
						if($content.length==0){
							enableshow = true;
							current-=2;
							return;
						}	
					}
					else
						var $content 			= $(this);
					
					//show ajax loading image
					// determine whether we need to wait for the current preview to hide
					var needWaitForHide = (current != -1);
					$loader.show();
					
					//there's a picture being displayed
					//we may need to wait for it to hide before creating the new preview
					// (do not hide immediately here; wait after large image is loaded)
					
					current 				= $content.index();
					var $thumb				= $content.find('img');
					var imgL_source 	 	= $thumb.attr('alt');
					var imgL_description 	= $thumb.next().html();
					//preload the large image to show
					$('<img style=""/>').on('load', function(){
						var $imgL 	= $(this);
						//resize the image based on the windows size
						resize($imgL);

						// function that actually creates and inserts the preview
						function doCreatePreview(){
								// remove existing preview immediately to avoid overlap
								var $old = $('#pp_gallery .pp_preview');
								if($old.length){
									$old.remove();
								}

								var preview = document.createElement('div');
								// set id and class so older selectors / animation code can find it
								preview.setAttribute('id', 'pp_preview');
								preview.setAttribute('class', 'pp_preview');
								preview.innerHTML = '<div class="pp_descr"><span>' + imgL_description + '</span></div>';
								var $preview = $(preview);
								// prepend the already-loaded large image element so we keep natural sizing
								$preview.prepend($imgL);

								// safety: remove any existing preview inside the gallery to avoid duplicates
								$('#pp_gallery #pp_preview').remove();
								// prepend to #pp_gallery so preview is sibling of prev/next and centers relative to gallery
								$('#pp_gallery').prepend($preview);

								// compute sizes based on loaded image
								var largeW = $imgL.width() + 20;
								var largeH = $imgL.height() + 10 + 45;
								$preview.css({
									'width': largeW + 'px',
									'height': largeH + 'px',
									'visibility': 'visible'
								});

								// description styling
								$preview.find('.pp_descr').css({
									'position': 'absolute',
									'bottom': '10px',
									'left': '0',
									'width': '100%'
								});

								// typography replacement if used
								if(typeof Cufon !== 'undefined'){
									Cufon.replace('.pp_descr');
								}
								// show navigation and hide loader
								showNavigation();
								$loader.hide();

								// random rotation angle between -20 and 20
								var r = Math.floor(Math.random() * 41) - 20;
								// set CSS variable for rotation
								$preview[0].style.setProperty('--pp-rot', r + 'deg');

								// ensure the browser has registered the element before we trigger transition
								// force a reflow then add .enter via double rAF to reliably start transform transition
								requestAnimationFrame(function(){
									void $preview[0].offsetWidth;
									requestAnimationFrame(function(){
										$preview.addClass('enter');
									});
								});

								// when transition ends, re-enable interactions if needed
								var handled = false;
								$preview.on('transitionend.pp_preview webkitTransitionEnd.pp_preview', function(ev){
									if(handled) return;
									// we accept either transform or opacity completing
									handled = true;
									$preview.off('transitionend.pp_preview webkitTransitionEnd.pp_preview');
									enableshow = true;
								});
								// fallback timeout
								setTimeout(function(){
									if(!handled){
										handled = true;
										$preview.off('transitionend.pp_preview webkitTransitionEnd.pp_preview');
										enableshow = true;
									}
								}, 900);
						}
						// Call the function to create the preview; wait for previous to hide if needed
						if(needWaitForHide){
							hideCurrentPicture(doCreatePreview);
						} else {
							doCreatePreview();
						}
					}).on('error', function(){
						//error loading image. Maybe show a message : 'no preview available'?
					}).attr('src',imgL_source);	
				}
				
				//click next image
				$next.on('click',function(){
					current+=2;
					showImage(1);
				});
				
				//click previous image
				$prev.on('click',function(){
					showImage(1);
				});
				
				//slides up the current picture
				// accepts an optional callback to run after removal
				function hideCurrentPicture(done){
						current = -1;
						var $pp = $('#pp_preview');
						if($pp.length === 0){
							if(typeof done === 'function') done();
							return;
						}
						// remove the enter state first so .exit rules are not overridden
						$pp.removeClass('enter');
						// force reflow so browser registers the removal before adding exit
						void $pp[0].offsetWidth;
						// trigger exit animation by adding .exit class which slides the preview upward
						$pp.addClass('exit');
						// listen for transition end to remove element and call callback
						var handledExit = false;
						$pp.on('transitionend.pp_exit webkitTransitionEnd.pp_exit', function(ev){
							if(handledExit) return;
							handledExit = true;
							$pp.off('transitionend.pp_exit webkitTransitionEnd.pp_exit');
							$pp.remove();
							if(typeof done === 'function') done();
						});
						// fallback: ensure removal after 700ms
						setTimeout(function(){
							if(!handledExit){
								handledExit = true;
								$pp.off('transitionend.pp_exit webkitTransitionEnd.pp_exit');
								$pp.remove();
								if(typeof done === 'function') done();
							}
						}, 700);
				}
				
				//shows the navigation buttons
				function showNavigation(){
					$next.stop().animate({'right':'0px'},100);
					$prev.stop().animate({'left':'0px'},100);
				}
				
				//hides the navigation buttons
				function hideNavigation(){
					$next.stop().animate({'right':'-40px'},300);
					$prev.stop().animate({'left':'-40px'},300);
				}
				
				//mouseenter event on each thumb
				function upImage(){
					var $content 	= $(this);
					$content.stop().animate({
						'marginTop'		: '-70px'
					},400).find('img')
						  .stop()
						  .css({'-webkit-transform':'rotate(0deg)','transform':'rotate(0deg)'});
				}
				
				//mouseleave event on each thumb
				function downImage(){
					var $content 	= $(this);
					var r 			= Math.floor(Math.random()*41)-20;
					$content.stop().animate({
						'marginTop'		: '0px'
					},400).find('img').stop().css({'-webkit-transform':'rotate(' + r + 'deg)','transform':'rotate(' + r + 'deg)'});
				}
				
				//resize function based on windows size
				function resize($image){
					var widthMargin		= 50
					var heightMargin 	= 200;
					
